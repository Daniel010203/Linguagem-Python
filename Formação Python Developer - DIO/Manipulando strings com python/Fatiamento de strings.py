nome = "Daniel de Oliveira Vieira"

print(nome[0])
# o indice buscado será a primeira letra da variável.
print(nome[:9])
# o indice buscado será até a da posição oitava dentro da variável,pois começa a se contar do zero.
print(nome[10:])
# o indice buscado será somente após a decima string da variável.
print(nome[10:16])
# o indice buscado será entre o decimo e o decimo sexto,dentro da variável.
print(nome[10:16:2])
# o indice buscado será entre o decimo e o decimo sexto,contando de dois em deois,dentro da variável.
print(nome[:])
# o indice buscado será a variável inteira.
print(nome[::-1])
# o indice buscado será a variável espelhada( de tras pra frente).
