curso = "Python"

print(curso.upper()) # converte os caracteres para maiusculo.
print(curso.lower()) # converte os caracteres para minusculo.
print(curso.title()) # converte somente a primeira linha para maiuscula e mantem as demais como minusculas.
print(curso.strip()) # remove os espaços vazios da esquerda e da direita.
print(curso.rstrip()) #remove somente os espaços da direita.
print(curso.lstrip()) # remove somente os espaços da esquerda.
print(curso.center(10, "#")) # serve para preencher os espaços total com a quantodade informada,totalizando que devem ter 10 campos preenchuidos no total.
print(".".join(curso)) # # fazer a junção dos caratcteres com o que estiver dentro de aspas,seja o que for.
