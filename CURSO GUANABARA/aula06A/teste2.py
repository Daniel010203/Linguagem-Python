n=int(input("Digite um valor: "))
print(n)
#Quando utilizamos o tipo primitivo int, o resulto da print será sempre convertido em número inteiro.

n=float(input("Digite um valor: "))
print(n)
#Quando utilizamos o tipo primitivo float, o resulto da print será sempre convertido em número decimal.

n=bool(input("Digite um valor: "))
print(n)
#Quando utilizamos o tipo primitivo bool, o resulto da print será sempre (True)VERDADEIRO,caso a operação seja feita,caso não seja atendido o pedido,será False(FALSO).

n=str(input("Digite um valor: "))
print(n)
#Quando utilizamos o tipo primitivo str, o resulto da print será sempre o mesmo digitado,pois só será confirmado que é uma string.